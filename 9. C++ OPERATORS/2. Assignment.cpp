// A list of all assignment operators:


// Operator	                                Example	                                Same As

//     =	                                 x = 5	                                x = 5

//     +=	                                 x += 3	                                x = x + 3

//     -=	                                 x -= 3	                                x = x - 3

//     *=	                                 x *= 3	                                x = x * 3

//     /=	                                 x /= 3	                                x = x / 3	

//     %=	                                 x %= 3	                                x = x % 3	

//     &=	                                 x &= 3	                                x = x & 3	

//     |=	                                 x |= 3	                                x = x | 3	

//     ^=	                                 x ^= 3	                                x = x ^ 3	

//     >>=	                                 x >>= 3	                            x = x >> 3	

//     <<=	                                 x <<= 3	                            x = x << 3